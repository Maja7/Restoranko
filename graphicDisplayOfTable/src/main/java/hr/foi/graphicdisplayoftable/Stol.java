package hr.foi.graphicdisplayoftable;

public class Stol {
    private int xOs;
    private int yOs;
    private String oznaka;
    private int brojStolaca;
    private int stolId;

    public Stol() {
    }

    public int getxOs() {
        return xOs;
    }

    public void setxOs(int xOs) {
        this.xOs = xOs;
    }

    public int getyOs() {
        return yOs;
    }

    public void setyOs(int yOs) {
        this.yOs = yOs;
    }

    public String getOznaka() {
        return oznaka;
    }

    public void setOznaka(String oznaka) {
        this.oznaka = oznaka;
    }

    public int getBrojStolaca() {
        return brojStolaca;
    }

    public void setBrojStolaca(int brojStolaca) {
        this.brojStolaca = brojStolaca;
    }

    public int getStolId() {
        return stolId;
    }

    public void setStolId(int stolId) {
        this.stolId = stolId;
    }
}
